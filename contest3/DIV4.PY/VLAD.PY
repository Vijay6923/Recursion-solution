def most_frequent_char(s):
    count_a = s.count('ğ™°')
    count_b = s.count('ğ™±')
    if count_a > count_b:
        return 'ğ™°'
    elif count_b > count_a:
        return 'ğ™±'
    else:
        return 'ğ™°'

t = int(input())


for _ in range(t):
    s = input()
    result = most_frequent_char(s)
    print(result)
